import fs from "node:fs";
import path from "node:path";

const sourcePath = path.resolve("prisma/schema.prisma");
const outputDirectory = path.resolve("prisma/production");
const outputPath = path.join(outputDirectory, "schema.prisma");
const source = fs.readFileSync(sourcePath, "utf8");
const providerMarker = 'provider = "sqlite"';
if (source.split(providerMarker).length !== 2) throw new Error("Expected exactly one SQLite datasource provider in prisma/schema.prisma.");

const productionSchema = source
  .replace(providerMarker, 'provider = "postgresql"')
  .replace(/organizationId(\s+)String\?/g, "organizationId$1String")
  .replace(/Organization\?/g, "Organization");

fs.mkdirSync(outputDirectory, { recursive: true });
fs.writeFileSync(outputPath, `// Generated by scripts/generate-production-schema.mjs. Do not edit directly.\n${productionSchema}`);
console.log("Generated PostgreSQL Prisma schema at prisma/production/schema.prisma.");
